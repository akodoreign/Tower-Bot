# Patch parsing
# ---------------------------------------------------------------------------

def parse_pending_patches() -> List[Dict]:
    """
    Parse PATCHES.md and return list of pending patches.
    
    Returns:
        List of dicts with: id, timestamp, mission, score, content, status_line
    """
    if not PATCHES_FILE.exists():
        return []
    
    try:
        text = PATCHES_FILE.read_text(encoding="utf-8")
    except Exception as e:
        logger.error(f"Could not read PATCHES.md: {e}")
        return []
    
    patches = []
    
    # Split by patch sessions (## Patches from ...)
    sessions = re.split(r'\n---\n\n## Patches from ', text)
    
    for i, session in enumerate(sessions[1:], start=1):  # Skip header
        # Parse session header
        lines = session.split('\n')
        if not lines:
            continue
        
        timestamp = lines[0].strip()
        mission = ""
        score = "?"
        
        # Find mission and score
        for line in lines[:10]:
            if line.startswith("**Test Mission:**"):
                mission = line.replace("**Test Mission:**", "").strip()
            elif line.startswith("**Quality Score:**"):
                score = line.replace("**Quality Score:**", "").strip()
        
        # Find individual patches (### Patch N)
        patch_sections = re.split(r'\n### Patch (\d+)\n', session)
        
        for j in range(1, len(patch_sections), 2):
            if j+1 >= len(patch_sections):
                break
                
            patch_num = patch_sections[j]
            patch_content_section = patch_sections[j+1]
            
            # Extract code block content
            code_match = re.search(r'```\n?(.*?)\n?```', patch_content_section, re.DOTALL)
            if not code_match:
                continue
            
            content = code_match.group(1).strip()
            
            # Check status
            status_match = re.search(r'\*\*Status:\*\*\s*(.*?)(?:\n|$)', patch_content_section)
            status = status_match.group(1).strip() if status_match else "PENDING"
            
            # Only include pending patches
            if "PENDING" in status or "⏳" in status:
                patch_id = f"{timestamp.replace(' ', '_').replace(':', '-')}_{patch_num}"
                patches.append({
                    "id": patch_id,
                    "num": patch_num,
                    "timestamp": timestamp,
                    "mission": mission,
                    "score": score,
                    "content": content,
                    "status": status,
                })
    
    return patches


def update_patch_status(patch_id: str, new_status: str, note: str = "") -> bool:
    """
    Update the status of a patch in PATCHES.md.
    
    Args:
        patch_id: Patch identifier (timestamp_num format)
        new_status: New status (✅ APPROVED, ❌ REJECTED, 🔄 MODIFIED)
        note: Optional note to add
    
    Returns:
        True if updated successfully
    """
    if not PATCHES_FILE.exists():
        return False
    
    try:
        text = PATCHES_FILE.read_text(encoding="utf-8")
    except Exception:
        return False
    
    # Parse patch_id to get timestamp and num
    parts = patch_id.rsplit('_', 1)
    if len(parts) != 2:
        return False
    
    timestamp_part = parts[0].replace('_', ' ').replace('-', ':')
    patch_num = parts[1]
    
    # Find and replace the status line
    # Look for pattern: ### Patch {num} ... **Status:** ⏳ PENDING
    pattern = rf'(### Patch {patch_num}\n.*?```\n.*?\n```\n\n\*\*Status:\*\*)\s*[^\n]*'
    
    timestamp_str = note if note else datetime.now().strftime("%Y-%m-%d %H:%M")
    replacement = rf'\1 {new_status} ({timestamp_str})'
    
    new_text, count = re.subn(pattern, replacement, text, flags=re.DOTALL)
    
    if count == 0:
        # Try simpler pattern
        simple_pattern = r'(\*\*Status:\*\*)\s*⏳\s*PENDING'
        # Only replace first match for this patch
        new_text = text
        found = False
        
        # Find the patch section
        session_match = re.search(rf'## Patches from {re.escape(timestamp_part)}.*?(?=\n---\n|$)', text, re.DOTALL)
        if session_match:
            session = session_match.group(0)
            patch_match = re.search(rf'### Patch {patch_num}\n.*?\*\*Status:\*\*\s*[^\n]*', session, re.DOTALL)
            if patch_match:
                old_section = patch_match.group(0)
                new_section = re.sub(r'\*\*Status:\*\*\s*⏳\s*PENDING', f'**Status:** {new_status} ({timestamp_str})', old_section)
                new_text = text.replace(old_section, new_section)
                found = True
        
        if not found:
            return False
    
    try:
        PATCHES_FILE.write_text(new_text, encoding="utf-8")
        logger.info(f"Updated patch {patch_id} to {new_status}")
        return True
    except Exception as e:
        logger.error(f"Failed to write patches file: {e}")
        return False


